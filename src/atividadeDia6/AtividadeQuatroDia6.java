package atividadeDia6;

public class AtividadeQuatroDia6 {

	public static void main(String[] args) {
		
		// Declarando variaveis:
		float nu1 = 5.0f;
		float nu2 = 6.0f;
		float nu3 = 7.0f;
		float nu4 = 8.0f;
		
		float n1 =  5.0f;
		float n2 =  6.0f;
		float n3 = -7.0f;
		float n4 =  8.0f;
		
		float dif1 = ((nu1 * nu2) - (nu3 * nu4));
		float dif2 = ((n1 * n2) - (n3 * n4));
		
		System.out.println("A primeira diferência é: " + dif1);
		System.out.println("A segunda diferência é: " + dif2);

	}

}
